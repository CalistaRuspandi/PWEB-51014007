<hr>
<center>
	<h2>	

	Di dekat Anjungan Pantai Losari, Anda dapat mencicipi salah satu kuliner khas Makassar yaitu<br> 
	Pisang Epe beserta teman menyantapnya yaitu sarabba.<br>
	 Anda bisa menikmati Pisang Epe panggang dengan pilihan saus seperti gula merah, durian, cokelat, dan keju.<br>

	Ketika masih panas, Pisang Epe cocok disantap dengan sarabba.<br>
	 Sarabba merupakan minuman hangat khas Makassar yang juga berguna meningkatkan stamina, <br>
	 melancarkan peredaran darah, mengatasi perut kembung, dan mengobati influenza.<br>
	Berbahan dasar gula merah, jahe, santan, susu, garam, merica, biji pala, dan kayu manis,<br>
	 minuman ini dapat Anda coba ketika berada di Kota Makassar.<br>
	  Untuk menikmati kedua kuliner tersebut, Anda cukup mengeluarkan uang sebesar Rp 15.000.<br>


