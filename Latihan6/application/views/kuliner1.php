	<h2>	
			Terletak di Jalan Gunung Lompobattang, di belakang Gedung Bank Indonesia. Nama Karebosi diambil dari nama Alun-alun<br>
			Kota Makassar, yang dulunya merupakan tempat pertama Warung Sop Konro ini berdiri. Kini Warung Sop Konro milik Haji<br>Hanafi ini harus bergeser tidak jauh dari Lapangan Karebosi.<br>

			Sop Konro dibuat dari daging sapi pilihan yang dicampur kuah sup, sedangkan Konro Bakar merupakan daging iga sapi<br>pilihan yang dipanggang dan dipadu dengan bumbu kacang racikan khas Makassar.<br>

			Untuk satu porsi Konro Bakar, kita harus merogoh kocek sebanyak Rp 38 ribu, belum termasuk nasi putihnya. Sedangkan<br> untuk menu Sop Konro atau Sop Kikil hanya Rp 35 ribu per porsi.<br>

			Sebenarnya, di Makassar ada beberapa rumah makan yang menyajikan menu Konro Bakar.<br> 
			Namun, tidak seistimewa dan selezat warung Konro Bakar Karebosi.<br>

			Sedangkan yang berdomisili di Jakarta, anda juga bisa menikmati Konro Bakar di wWrung Konro Karebosi Kelapa Gading,<br>
			di Jalan Boulevard Raya Blok TA 2 No 38, Kelapa Gading, Jakarta Utara. Warung Konro Karebosi di Kelapa Gading ini<br> satu-satunya cabang yang dibuka oleh Haji Hanafi. Selain menu Konro Bakar,<br>
			di warung Haji Hanafi ini juga tersaji Sop Konro dan Sop Kikil.<br>





