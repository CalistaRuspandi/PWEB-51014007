	<h2>	


	Es Palu Butung adalah es campur dengan gaya khas Makassar <br>
	yang merupakan makanan penutup sangat populer dari Makassar, Sulawesi Selatan. <br>
	Bahan utama pembuatan es ini berupa pisang hijau yang dipotong-potong <br>
	yang diletakkan diatas semacam bubur berwarna putih yang terbuat <br>
	dari santan ditambah dengan es serut dan susu kental. Es ini juga biasa tersaji di warung.<br>


