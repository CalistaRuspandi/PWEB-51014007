		Menu Pallubasa adalah sup daging sapi yang kuahnya dicampur dengan kelapa goreng. Di Makassar ada dua tempat makan<br> Pallubasa, yakni warung Pallubasa di jalan Serigala dan di jalan Onta. Kedua warung ini lokasinya berdekatan.<br>
		Umumnya nama jalan tempat warung tersebut berlokasi dijadikan merek warung di Makassar.<br>

		Di Pallubasa Serigala atau di Pallubasa Onta, pengunjungnya dapat memesan bagian-bagian daging sapi mana kesukaannya.<br> Misalnya memenang daging pipi atau lidah sapi. Atau, menunya full daging sapi saja.<br>
