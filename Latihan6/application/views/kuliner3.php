<hr>
<center>
	<h2>	
	Di Makassar sendiri, salah satu warung nasi kuning yang terkenal adalah nasi kuning Riburane<br>
	yang sudah populer menjadi sajian kuliner khas dari Makassar.<br>
	Padahal jelas-jelas terpampang nyata di spanduk depan warung : Nasi Kuning Riburane, Asuhan Hj.<br> 
	Istiah Jawa Timur, yang menandakan bahwa nasi kuning Riburane bukanlah kuliner asli Makassar.<br>
	

	Usaha ini dirintis oleh Hj Istiah sejak tahun 1980an yang kemudian semakin laris dan disukai banyak orang<br>
	baik masyarakat lokal maupun wisatawan yang sedang berkunjung ke Makassar. <br>
	Bahkan konon, nasi kuning Riburane juga menjadi kuliner favorit dari Pak SBY. <br>
	Tidak heran sih, kelezatan rasanya sudah dibuktikan dengan meraih penghargaan Makassar Most Favourite (MMF)<br>
	 Award Culinary untuk kategori nasi selama tiga tahun berturut, mulai dari tahun 2010 hingga 2012. <br>
	 Award Culinary ini merupakan kerjasama antara Majalah Makassar Terkini dengan Makassar Research.<br>


	Citarasa nasi kuning Riburane sudah disesuaikan dengan lidah orang Makassar<br>
	yang memang berbeda selera dengan orang Jawa yang cenderung menyukai makanan manis.<br> 
	Dalam sepiring nasi kuning akan dijumpai lauk pauk <br>
	berupa telur pindang rebus, semur daging, sayur labu siam, sayur nangka, paru goreng yang empuk, abon dan kering kentang.<br>
	 Saat dicicipi, wangi nasi kuningnya menguar dan rasa lauknya yang sedikit asin di lidah<br>
	  menjadikan rasa nasi kuningnya semakin kuat.<br>
			

	</h2>
